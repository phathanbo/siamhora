function showDayInfo(dayKey) {
    const data = dayBirthData[dayKey];
    const card = document.getElementById('prediction-card');
    const colors = {
        sunday: '#FF0000', monday: '#f1c40f', tuesday: '#e84393',
        wednesday: '#27ae60', thursday: '#e67e22', friday: '#2980b9', saturday: '#6c5ce7'
    };

    // แสดงการ์ด
    card.classList.remove('d-none');

    // อัปเดตข้อมูลพื้นฐาน
    document.getElementById('display-name').innerText = data.name;
    document.getElementById('display-meta').innerText = data.meta;
    document.getElementById('display-character').innerText = data.character;
    document.getElementById('card-name-bg').style.backgroundColor = colors[dayKey];

    // อัปเดตเกณฑ์อายุ
    const milestoneList = document.getElementById('display-milestones');
    milestoneList.innerHTML = data.milestones.map(m =>
        `<li class="mb-1"><b>อายุ ${m.age}:</b> ${m.detail}</li>`
    ).join('');

    // อัปเดตทิศมงคล
    const directionDiv = document.getElementById('display-directions');
    directionDiv.innerHTML = Object.entries(data.directions).map(([key, val]) =>
        `<div class="col-6 text-capitalize"><b>${key}:</b> ${val}</div>`
    ).join('');

    // เพิ่มคลาส active ให้ปุ่ม
    document.querySelectorAll('.btn-day').forEach(btn => btn.classList.remove('active'));
    event.target.classList.add('active');
}

function analyzePlanetElement(planetNum) {
    const planet = planetData[planetNum];
    if (!planet) return "ไม่พบข้อมูลพระเคราะห์";

    let description = `<b>พระ${planet.name}</b> (เลข ${planetNum}) ครอง<b>ธาตุ${planet.element}</b> `;
    description += `ประจำทิศ${planet.direction} มีกำลังมหาจักรคือ ${planet.power} `;
    description += `ทรง${planet.vehicle}เป็นพาหนะ ผิวกายสี${planet.color}`;

    // ตรวจสอบคู่ธาตุ (คู่ อสิติธาตุ)
    if (planet.คู่ธาตุ) {
        const soulmate = planetData[planet.คู่ธาตุ];
        description += `<br><span class="text-primary"><b>คู่ธาตุ:</b> พระ${planet.name} และ พระ${soulmate.name} เป็นคู่ธาตุ${planet.element} (คู่ อสิติธาตุ) 
        หากกุมลัคนาจะรุ่งเรืองยิ่งนัก แม้เกิดในตระกูลต่ำก็เด่นขึ้นมาได้</span>`;
    }

    return description;
}

function showPlanet(num) {
    const result = analyzePlanetElement(num);
    document.getElementById('planet-info-display').innerHTML = result;
}

